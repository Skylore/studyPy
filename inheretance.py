class Car:
    def __init__(self, color, speed, name):
        self.color = color
        self.speed = speed
        self.name = name

    def getcollor(self):
        return self.color

    def getspeed(self):
        return self.speed

    def setcolor(self):
        self.color = color

    def setspeed(self):
        self.speed = speed
